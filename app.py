@app.route('/api/heatmap', methods=['GET'])
def get_heatmap():
    """Return 24-hour volume data for generating peak hour graphs."""
    hours = [f"{i:02d}:00" for i in range(24)]
    # Use our logic to build a realistic daily curve
    volumes = []
    for i in range(24):
        # Add some slight randomness around the base curve
        vol = get_base_volume(i)
        volumes.append(int(vol * random.uniform(0.9, 1.1)))
    
    return jsonify({
        "labels": hours,
        "data": volumes
    })

