from flask import Flask, request, jsonify
from flask_cors import CORS
import random
from datetime import datetime, timedelta

app = Flask(__name__)
# Enable CORS for frontend requests
CORS(app)

def get_base_volume(hour):
    """Simulate traffic volume based on hour of the day."""
    # Morning peak 8-10, Evening peak 17-19
    if 8 <= hour <= 10 or 17 <= hour <= 19:
        return random.randint(35000, 50000) # High
    # Night (free flow)
    elif hour < 5 or hour > 22:
        return random.randint(5000, 15000) # Low
    # Daytime (moderate)
    else:
        return random.randint(15000, 30000) # Moderate

@app.route('/api/status', methods=['GET'])
def get_status():
    """Get current overall traffic status."""
    current_hour = datetime.now().hour
    vol = get_base_volume(current_hour)
    
    level = "Low"
    if vol > 35000:
        level = "High"
    elif vol > 20000:
        level = "Moderate"
        
    alerts = []
    if level == "High":
        alerts.append("Heavy traffic detected due to prime peak hours.")
    elif level == "Moderate":
        alerts.append("Expect moderate delays on major arterials.")
    else:
        alerts.append("Traffic is flowing smoothly across major routes.")

    return jsonify({
        "status": "Operational",
        "current_traffic_level": level,
        "active_alerts": alerts,
        "timestamp": datetime.now().isoformat()
    })

@app.route('/api/predict', methods=['POST'])
def predict_traffic():
    """
    Main prediction logic for the Smart Assistant.
    Input: source, destination, desired_arrival_time
    Output: recommended departure, travel time, and natural language advice.
    """
    data = request.json
    source = data.get('source', 'Unknown Source')
    destination = data.get('destination', 'Unknown Dest')
    desired_arrival_time = data.get('arrival_time', '09:00') # HH:MM format
    
    try:
        arr_time = datetime.strptime(desired_arrival_time, '%H:%M')
    except ValueError:
        arr_time = datetime.now()

    # Simulate base duration between 20 to 45 mins
    base_duration_mins = random.randint(20, 45)
    
    # We iterate backwards to find the actual departure time accounting for traffic
    # A simple simulation:
    dep_time = arr_time - timedelta(minutes=base_duration_mins)
    dep_hour = dep_time.hour
    
    vol = get_base_volume(dep_hour)
    
    # Add traffic delay
    added_delay = int((vol / 50000) * 45) # up to 45 extra mins in worst traffic
    total_duration = base_duration_mins + added_delay
    
    # Recalculate strict departure time
    dep_time = arr_time - timedelta(minutes=total_duration)
    
    level_val = "Low"
    if vol > 35000:
        level_val = "High"
    elif vol > 20000:
        level_val = "Moderate"
        
    # Natural language formatting
    rec_time_str = dep_time.strftime('%I:%M %p')
    arr_time_str = arr_time.strftime('%I:%M %p')

    recommendation = f"Leave at {rec_time_str} to reach by {arr_time_str} with {level_val.lower()} traffic."

    return jsonify({
        "recommended_departure": rec_time_str,
        "estimated_duration_mins": total_duration,
        "traffic_level": level_val,
        "recommendation_text": recommendation
    })

@app.route('/api/simulation', methods=['POST'])
def what_if_simulation():
    """
    Simulate travel times for a specific departure time.
    """
    data = request.json
    departure_time = data.get('departure_time', '09:00')
    
    try:
        dep_time = datetime.strptime(departure_time, '%H:%M')
    except ValueError:
        dep_time = datetime.now()

    dep_hour = dep_time.hour
    vol = get_base_volume(dep_hour)
    
    base_duration_mins = random.randint(20, 45)
    added_delay = int((vol / 50000) * 45)
    total_duration = base_duration_mins + added_delay

    return jsonify({
        "departure_time": departure_time,
        "estimated_duration_mins": total_duration,
        "predicted_volume": vol,
        "formatted_departure": dep_time.strftime('%I:%M %p')
    })


